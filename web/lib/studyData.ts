// Helper function to get flashcards by topic
export function getFlashcardsByTopic(topic: StudyTopic): Flashcard[] {
  return FLASHCARDS.filter(card => card.topic === topic);
}

// Helper function to get quiz questions by topic
export function getQuizQuestionsByTopic(topic: StudyTopic): QuizQuestion[] {
  return QUIZ_QUESTIONS.filter(q => q.topic === topic);
}

// Helper to get all topics with counts
export function getTopicStats(): Record<StudyTopic, { flashcards: number; quizzes: number }> {
  const topics = Object.keys(TOPIC_INFO) as StudyTopic[];
  return topics.reduce((acc, topic) => {
    acc[topic] = {
      flashcards: FLASHCARDS.filter(c => c.topic === topic).length,
      quizzes: QUIZ_QUESTIONS.filter(q => q.topic === topic).length,
    };
    return acc;
  }, {} as Record<StudyTopic, { flashcards: number; quizzes: number }>);
}

// Helper to shuffle array
export function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}
